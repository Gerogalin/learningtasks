			<div id="col-one">
			<h2>- WHAT IS CORONAVIRUS (COVID-19) AND HOW DOES IT SPREAD?</h2>
			<h3>The Coronavirus (COVID-19) is a new respiratory illness first identified during an investigation into an outbreak in Wuhan, China, but has now spread to other parts of the world, including the United States. </h3>
			<div id="q">
			<div id="q1">
			<h3>The virus that causes COVID-19 is called SARS-CoV-2 and is spread between people who are in close contact with each other (within about six feet), mostly through respiratory droplets produced when an infected person coughs or sneezes. </h3></div>
			<div id="q2"><img src="img/newnormal.png" class="normal"></div>
			</div>
			<h3>People are thought to be most contagious when they are the sickest (symptomatic); however, growing evidence suggests that COVID-19 may be spread by people before they start showing symptoms (pre-symptomatic) or by people who have the virus but never show symptoms (asymptomatic). The virus can also live on certain surfaces for a period of time which can make it possible for people to get COVID-19 if they touch a contaminated object and then touch their mouth, nose, or eyes. That is why it is important to take preventive steps to avoid getting sick. </h3>
		</div>
		<div id="col-two">
			<h2>- WHAT ARE THE SYMPTOMS?</h2>
			<h3>Some people who get COVID-19 may be asymptomatic but for those experiencing symptoms, the following, varying from mild to severe, may appear 2-14 days after exposure:</h3>
					<ul>
						<li>Fever</li>
						<li>Cough </li>
						<li>Shortness of breath </li>
						<li>Headache</li>
						<li>Fatigue (tiredness)</li>
						<li>Body aches	</li>
						<li>Chills</li>
						<li>Diarrhea</li>
						<li>Sore throat</li>
						<li>Nasal congestion</li>
						<li>Loss of smell</li>
						<li>Loss of taste</li>
					</ul>
			</div>
			<div id="col-three">
				<h2>- WHAT DO I DO IF I FEEL SICK?</h2>
				<h3>Per the Centers for Disease Control and Prevention (CDC) recommendations, if you are sick with COVID-19 or start experiencing its symptoms, follow these steps: </h3>
				<h3><bold>Stay home:</bold> Most people with COVID-19 get a mild form of the illness that may include fever, coughing, and other symptoms listed above. People with mild symptoms can usually get better at home without medical care. Do not leave your home, except to get medical care. Do not visit public areas.  </h3>
				<h3><bold>Stay in touch with your doctor: </bold> your doctor before going to their office. If you do not have a regular doctor, call your local health department or hospital to find out where you can go to seek medical care. Be sure to get care immediately if you have trouble breathing, since that is a more serious symptom, or if you have any emergency warning signs, or think it is an emergency.  </h3>
				<h3><bold>Avoid public transportation:</bold> Avoid using public transportation, ride sharing, or taxis. </h3>
				<h3><bold>Stay away from others:</bold> Separate yourself from other people or pets in your home and stay in a specific “sick room” and use a separate bathroom, if possible. Clean high-touch areas in your “sick room” and bathroom and let someone else clean other areas of the house.</h3>
				<h3><bold>Cover your coughs and sneezes:</bold> Cover your cough or sneezes with a tissue and throw away the tissue in a lined trash. Immediately wash your hands for at least 20 seconds with soap and water or use hand sanitizer that has at least 60% alcohol if soap and water are not available.</h3>
				<h3><bold>Clean your hands often:</bold> Wash your hands with soap and water often. Only use hand sanitizer when soap and water are not available. </h3>
				<h3><bold>Avoid sharing personal items: </bold> Do not share dishes, utensils, glasses, cups, towels or bedding when you are sick. Always make sure to wash your household items with soap and water. </h3>
				<h3><bold>Wear a cloth mask or covering:</bold> If you are sick and you must be around others even at home, wear a cloth mask or face covering over your nose and mouth. </h3>
				<h3><bold>Monitor your symptoms: </bold>Common symptoms of COVID-19 include fever and cough. Trouble breathing is a more serious symptom that means you should get medical attention immediately.  </h3>
			</div>
