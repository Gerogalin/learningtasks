				<h2>- WHO IS AT HIGHER RISK?</h2>
				<h3>Anyone, no matter their age, can become sick with COVID-19. However, older adults (over 65 years of age) and people with compromised immune systems or chronic conditions, such as diabetes and lung disease, may be at higher risk for developing more serious symptoms or complications from COVID-19. If you or your loved ones are at higher risk, stay home as much as possible, consult with your doctor about additional steps to take to protect yourself and follow these prevention measures to decrease your risk of getting sick with COVID-19:</h3>
					<h3>- Ensure you have enough food at home and that your prescriptions are refilled and up to date.</h3>
					<h3>- void large crowds.</h3> 
					<h3>- Avoid nonessential travel. </h3>
					<h3>- Stay at home as much as possible. </h3>
