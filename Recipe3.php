<h1>Cucumber Salad</h1>
<img src="Recipe3.jpg" alt="Cucumber Salad" title="Cucumber Salad">

<h3>Ingredients</h3>
			<ul>
				<li>1 cucumber, sliced</li>
				<li>3 tbsp sugar</li>
				<li>1/4 cup fresh lemon juice</li>
				<li>8 oz. sour cream</li>
				<li>1 tspn salt</li>
				<li>1/2 tspn dill</li>
			</ul>

<h3>Methods</h3>
			<ol>
				<li>Dissolve sugar in lemon juice. Add sour cream, salt and dill. Mix with the 
				cucumbers.</li>	
			</ol>
