  var song1 = document.getElementById("song1");
  var icon1 = document.getElementById("play1");

 icon1.onclick = function () {
    if (song1.paused) {
      song1.play();
      icon1.src="images/pause.png";
    }else{
      song1.pause();
      icon1.src="images/play.png";
    }
  }

